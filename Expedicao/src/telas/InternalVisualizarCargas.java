    public void setarCampos() throws SQLException {

         
            CargasGS = cargasDAO.getCargas();
        


        modeloCargas = (DefaultTableModel) tabelaCargas.getModel();

        for (int i = 0; i < CargasGS.size(); i++) {

            modeloCargas.addRow(new Object[]{CargasGS.get(i).getIdCarga(), CargasGS.get(i).getId_Nota_Fiscal(), CargasGS.get(i).getCliente(), CargasGS.get(i).getDescPedido(), CargasGS.get(i).getCidade()});
        }

        tabelaCargas.setModel(modeloCargas);

    }

